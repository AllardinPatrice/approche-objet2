package tp5.entites;

/**
 * Classe Personnage
 * 
 * @author Patrice
 *
 */
public class Personnage {
	public Personnage() {

	}

	protected int force;
	protected int pointsDeVie;

	public int getForce() {
		return force;
	}

	public void setForce(int force) {
		this.force = force;
	}

	public int getPointsDeVie() {
		return pointsDeVie;
	}

	public void setPointsDeVie(int pointsDeVie) {
		this.pointsDeVie = pointsDeVie;
	}

}
