package tp5.formes;

/**
 * Forme géométrique
 * 
 * @author Patrice
 *
 */
public abstract class Forme {
	/**
	 * Calcul de surface de forme
	 * 
	 * @return
	 */
	public abstract double calculerSurface();

	/**
	 * Calcul de périmetre de forme
	 * 
	 * @return
	 */
	public abstract double calculerPermietre();
}
